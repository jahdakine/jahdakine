<p class="reset">2002-2009. Utilized a database-driven Flash map driving navigation from US, to state, county, and city maps arriving at a specific installation in a community. 
The bulk of the work revolved around an access controlled administrative area on a different server to facilitate entry forms responsible for populating 
new data points. The house-build records were quite extensive and I developed a custom importer to enhance the web form inputs, and a data feed that was 
used to display the total house counts shown here. 
